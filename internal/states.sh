states()
{
# Syntax:
#		 states --imported
#
			___;

			if [ "${1}" = "--imported" ]
			then
				echo True
			fi
}

#---------------------------------------

state()
{
# Syntax:
#		 state --currently <PROCESS>
#
		__cleanse__()
		{
					sleep 0.5; [ -e ${___CON_STA___}/${1} ] && rm -f ${___CON_STA___}/${1};
		}

		if [ "${1}" = "--currently" ]
		then
			if [ -n ${2} ]
			then
				[ -e ${___CON_STA___}/${2} ] || touch ${___CON_STA___}/${2} && __cleanse__ ${2} &
			fi
		fi
}

#--------------------------------------------------------------------------------------------------

if REBUNTU="$(l1=$(grep "REBUNTU_LOCATION=" /etc/rebuntu.conf);
              l2=${l1##*=}; echo "${l2}")";
then
    [ -e ${REBUNTU}/sources/definitions.sh ] \
    && . ${REBUNTU}/sources/definitions.sh;
fi

