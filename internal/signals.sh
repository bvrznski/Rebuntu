signals()
{
# Syntax:
#		 signals --imported
#
			___;

			if [ "${1}" = "--imported" ]
			then
				echo True
			fi
}

#---------------------------------------

signal()
{
# Syntax:
#
		if [ "${1}" = "--say" ]
		then
			shift 1;

			for i in $@;
			do
				if [ "$(activity --on speaker)" = "True" ]
				then
					echo "${i}" >> ${___SIG_SAY___}
				fi;
			done;
		fi;
}

#--------------------------------------------------------------------------------------------------

if REBUNTU="$(l1=$(grep "REBUNTU_LOCATION=" /etc/rebuntu.conf);
              l2=${l1##*=}; echo "${l2}")";
then
    [ -e ${REBUNTU}/sources/definitions.sh ] \
    && . ${REBUNTU}/sources/definitions.sh;
    [ -e ${REBUNTU}/internal/controls.sh ] \
    && . ${REBUNTU}/internal/controls.sh;
fi

